#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	char a;

	a = '\n';
	if (s && *s != '\0')
	{
		write(fd, s, 1);
		ft_putendl_fd(s + 1, fd);
	}
	else
		write(fd, &a, 1);
}
