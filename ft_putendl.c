#include <unistd.h>

void	ft_putendl(char const *s)
{
	char a;

	a = '\n';
	if (s && *s != '\0')
	{
		write(1, s, 1);
		ft_putendl(s + 1);
	}
	else
		write(1, &a, 1);
}
