#include <string.h>

void	*ft_memset(void *b, int c, size_t len)
{
	int i;

	i = 0;
	while ((size_t)i < len)
	{
		*(char*)b = c;
		b++;
		i++;
	}
	b = b - i;
	return (b);
}
