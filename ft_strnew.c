#include <stdlib.h>

char		*ft_strnew(size_t size)
{
	char	*a;
	int		i;

	a = (char*)malloc(size + 1);
	i = 0;
	while ((size_t)i <= size && a != NULL)
	{
		*(a + i) = '\0';
		i++;
	}
	return (a);
}
