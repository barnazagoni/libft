#include <stdlib.h>

char		*ft_itoa(int n)
{
	char	*a;
	int		i;
	long	m;

	m = (long)n * 10;
	i = 0;
	if (m <= 0)
		i++;
	while ((m /= 10) != 0)
		i++;
	a = (char*)malloc(sizeof(char) * (i + 1));
	if (a != NULL)
		*(a + i--) = '\0';
	m = n;
	if (n < 0 && a != NULL)
	{
		m = -m;
		*(a) = '-';
	}
	while (i >= 0 + (n < 0) && a != NULL)
	{
		*(a + i--) = (m % 10) + 48;
		m = m / 10;
	}
	return (a);
}
